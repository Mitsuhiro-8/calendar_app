  # POST /resource
  def create
    @user = User.new(sign_up_params)
    unless @user.valid?
      flash.now[:alert] = @user.errors.full_messages
      render :new and return
    end
    session["devise.regist_data"] = {user: @user.attributes}
    session["devise.regist_data"][:user]["password"] = params[:user][:password]
    @calendar = @user.calendars.build
    render :new_calendar
  end
  
  def create_calendar
    @user = User.new(session["devise.regist_data"]["user"])
    @calendar = Calendar.new(calendar_params)
    unless @calendar.valid?
      flash.now[:alert] = @calendar.errors.full_messages
      render :new_calendar and return
    end
    @user.calendars.build(@calendar.attributes)
    if @user.save
      session["devise.regist_data"]["user"].clear
      session[:calendarPath] = @user.calendars.first.id
      sign_in(:user, @user)
      redirect_to calendar_path(session[:calendarPath]), notice: "アカウントを作成しました"
    else
      render :new_calendar
    end
  end


